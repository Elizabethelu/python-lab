P=int(input("Enter the percentage : "))
print("Percentage : ",P)
if P>90 :
    print("Grade : O " )
elif P>=80:
     print("Grade : A ")
elif P>=70 :
     print(" Grade : B ")
elif P>=50 :
     print(" Grade : C ")
elif P<50 :
     print("Grade : F ")      
    